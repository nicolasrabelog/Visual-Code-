#Desenvolva um programa que leia as duas notas de um aluno em uma matéria e que faça a média
#na tela a sua média na disciplina
#Ex
#Nota 1: 4.5
#Nota 2: 8.5
#A média entre 4.5 e 8.5 é igual a 6.5
nota1 = float(input("Digite a nota 1 "))
nota2 = float(input("Digite a nota 2 "))
media = (nota1+nota2) / 2
print("A media entre a",nota1,"e a",nota2,"e igual a",media)
